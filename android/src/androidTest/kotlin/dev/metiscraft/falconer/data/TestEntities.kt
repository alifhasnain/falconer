package dev.metiscraft.falconer.data

/** Builds a transaction entity for instrumented tests. */
internal fun testEntity(
    id: String,
    startedAt: Long = 0,
    method: String = "GET",
    url: String = "https://api.example.com/y",
    path: String = "/y",
    statusCode: Int? = null,
    requestBody: String? = null,
    responseBody: String? = null,
    responseBodyKind: String? = null,
    responseImageBytes: ByteArray? = null,
) = HttpTransactionEntity(
    id = id,
    startedAt = startedAt,
    completedAt = null,
    tookMs = null,
    method = method,
    url = url,
    host = "api.example.com",
    path = path,
    scheme = "https",
    protocol = null,
    requestHeaders = "{}",
    requestContentType = null,
    requestContentLength = null,
    requestBody = requestBody,
    requestBodyKind = "none",
    statusCode = statusCode,
    statusMessage = null,
    responseHeaders = null,
    responseContentType = null,
    responseContentLength = null,
    responseBody = responseBody,
    responseBodyKind = responseBodyKind,
    responseImageBytes = responseImageBytes,
    error = null,
)
